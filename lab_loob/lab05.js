// วนลูปเริ่มต้นที่ 2 ถึง 12
for (let i = 2; i <= 12; i++) {
    // วนลูปเริ่มต้นที่ 1 ถึง 12 เพื่อแสดงผลลูปคูณ
    for (let j = 1; j <= 12; j++) {
        // แสดงผลลูปคูณใน console
        console.log(`${i} x ${j} = ${i * j}`);
    }

    // เพิ่มเว้นวรรคใน console ระหว่างแต่ละสูตร
    console.log('\n');
}
